CREATE TABLE `jugador` (
  `id_jugador` int NOT NULL,
  `nombre` varchar(100) DEFAULT NULL,
  `fechaNacimiento` date DEFAULT NULL,
  `altura` float DEFAULT NULL,
  `peso` float DEFAULT NULL,
  PRIMARY KEY (`id_jugador`)
