CREATE TABLE `mundial` (
  `id_mundial` int NOT NULL AUTO_INCREMENT,
  `pais` varchar(45) DEFAULT NULL,
  `fechaInicio` date DEFAULT NULL,
  `fechaFin` date DEFAULT NULL,
  PRIMARY KEY (`id_mundial`)
);

INSERT INTO `mundial` VALUES (1,'Rusia','2018-06-14','2018-07-15');

CREATE TABLE `seleccion` (
  `id_seleccion` int NOT NULL,
  `nombre` varchar(70) DEFAULT NULL,
  `bandera` varchar(200) DEFAULT NULL,
  `abreviatura` varchar(5) DEFAULT NULL,
  PRIMARY KEY (`id_seleccion`)
);

CREATE TABLE `tipopartido` (
  `id_tipoPartido` int NOT NULL,
  `nombre` varchar(70) DEFAULT NULL,
  PRIMARY KEY (`id_tipoPartido`)
);

INSERT INTO `tipopartido` VALUES (1,'Fase de Grupos'),(2,'Octavos de Final'),(3,'Cuartos de Final'),(4,'SemiFinal'),(5,'Tercer y Cuarto Puesto'),(6,'Final');

CREATE TABLE `jugadorseleccion` (
  `id_mundial` int NOT NULL,
  `id_seleccion` int NOT NULL,
  `id_jugador` int NOT NULL,
  `dorsal` int DEFAULT NULL,
  PRIMARY KEY (`id_mundial`,`id_seleccion`,`id_jugador`),
  KEY `jugador_idx` (`id_jugador`),
  KEY `jugadorDorsal_idx` (`id_jugador`),
  KEY `jugadorSeleccion_idx` (`id_seleccion`),
  CONSTRAINT `jugadorDorsal` FOREIGN KEY (`id_jugador`) REFERENCES `jugador` (`id_jugador`),
  CONSTRAINT `jugadorMundial` FOREIGN KEY (`id_mundial`) REFERENCES `mundial` (`id_mundial`),
  CONSTRAINT `jugadorSeleccion` FOREIGN KEY (`id_seleccion`) REFERENCES `seleccion` (`id_seleccion`)
);

CREATE TABLE `mundialseleccion` (
  `id_mundial` int NOT NULL,
  `id_seleccion` int NOT NULL,
  `id_grupo` int DEFAULT NULL,
  PRIMARY KEY (`id_mundial`,`id_seleccion`),
  KEY `mundialSeleccionSeleccion_idx` (`id_seleccion`),
  KEY `mundialSeleccionGrupo_idx` (`id_grupo`),
  CONSTRAINT `mundialSeleccionGrupo` FOREIGN KEY (`id_grupo`) REFERENCES `grupo` (`id_grupo`),
  CONSTRAINT `mundialSeleccionMundial` FOREIGN KEY (`id_mundial`) REFERENCES `mundial` (`id_mundial`),
  CONSTRAINT `mundialSeleccionSeleccion` FOREIGN KEY (`id_seleccion`) REFERENCES `seleccion` (`id_seleccion`)
);


CREATE TABLE `partido` (
  `id_partido` int NOT NULL,
  `fecha` date DEFAULT NULL,
  `id_mundial` int NOT NULL,
  `id_tipoPartido` int NOT NULL,
  `id_seleccion1` int NOT NULL,
  `id_seleccion2` int NOT NULL,
  `golesSeleccion1` int NOT NULL,
  `golesSeleccion2` int NOT NULL,
  `penalesSeleccion1` int NOT NULL,
  `penalesSeleccion2` int NOT NULL,
  PRIMARY KEY (`id_partido`),
  KEY `mundialPartido_idx` (`id_mundial`),
  KEY `partidoTipoPartido_idx` (`id_tipoPartido`),
  KEY `partidoSeleccion_idx` (`id_seleccion1`,`id_seleccion2`),
  KEY `partidoMundial_idx` (`id_mundial`),
  KEY `partido_Tipopartido_idx` (`id_tipoPartido`),
  KEY `partidoSeleccion1_idx` (`id_seleccion1`),
  KEY `partidoSeleccion2_idx` (`id_seleccion2`),
  CONSTRAINT `partido_Tipopartido` FOREIGN KEY (`id_tipoPartido`) REFERENCES `tipopartido` (`id_tipoPartido`),
  CONSTRAINT `partidoMundial` FOREIGN KEY (`id_mundial`) REFERENCES `mundial` (`id_mundial`),
  CONSTRAINT `partidoSeleccion1` FOREIGN KEY (`id_seleccion1`) REFERENCES `seleccion` (`id_seleccion`),
  CONSTRAINT `partidoSeleccion2` FOREIGN KEY (`id_seleccion2`) REFERENCES `seleccion` (`id_seleccion`)
);


